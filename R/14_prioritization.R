# ==============================================================================
# Fig 14b: ECHA list presence heatmap - top 50 substances x 10 high-concern lists
#
# INTENT
# ------
# A binary presence/absence matrix that shows, for each of the 50 highest-
# priority substances, which of the ten most consequential ECHA regulatory
# instruments it appears on.  The heatmap reveals whether a substance's high
# score reflects broad multi-list coverage (a sign of deep regulatory concern)
# or is driven by a single high-weight instrument.  Substances with red cells
# spread across many columns warrant the most urgent attention.
# ==============================================================================

concern_lists <- c(
  "candidate_list", "svhc_identification", "restriction_list",
  "authorisation_list", "pops_list", "pbt_assessment",
  "ed_assessment", "harmonised_list", "pesticides", "clh_process"
)

list_labels <- c(
  candidate_list      = "Candidate list",
  svhc_identification = "SVHC ident.",
  restriction_list    = "Restriction list",
  authorisation_list  = "Authorisation list",
  pops_list           = "POPs list",
  pbt_assessment      = "PBT assessment",
  ed_assessment       = "ED assessment",
  harmonised_list     = "Harmonised list",
  pesticides          = "Pesticides",
  clh_process         = "CLH process"
)

heatmap_data <- top50 |>
  select(substance, substance_name, echa_lists, priority_score) |>
  separate_rows(echa_lists, sep = "\\|") |>
  filter(echa_lists %in% concern_lists) |>
  mutate(present = 1L) |>
  complete(
    nesting(substance, substance_name, priority_score),
    echa_lists = concern_lists,
    fill = list(present = 0L)
  ) |>
  mutate(
