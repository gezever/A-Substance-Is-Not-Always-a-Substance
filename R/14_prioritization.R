chebi_raw <- read_csv(here("data", "processed", "chebi.csv"), show_col_types = FALSE)

# Valid ECHA list identifiers.  A small number of CSV-parsing artefacts
# (quoted chemical names mis-parsed as list types) are excluded by restricting
# to this controlled vocabulary.
valid_lists <- c(
  "reach_registrations", "harmonised_list", "dossier_evaluation",
  "eu_positive_list", "pesticides", "authorisation_list", "clh_process",
  "candidate_list", "svhc_identification", "substance_evaluation",
  "pbt_assessment", "ed_assessment", "restriction_list",
  "authorisation_process", "pops_list", "pops_process",
  "restriction_process", "sommatie_stoffen", "anhydrous"
)

# The raw CSV contains multiple rows per substance URI because the SPARQL
# query in chebi_to_table.rq groups by (substance, substance_name): the same
# URI can appear under several preferred labels (synonyms, ISO names, etc.).
# We collapse to one row per URI, concatenating unique non-missing values.
chebi <- chebi_raw |>
  group_by(substance) |>
  summarise(
    substance_name      = first(substance_name),
    inchikey            = first(inchikey),
    casNumbers          = paste(unique(na.omit(casNumbers)), collapse = "|"),
    ecNumbers           = paste(unique(na.omit(ecNumbers)),  collapse = "|"),
    bio_activity_labels = paste(unique(na.omit(bio_activity_labels)), collapse = "|"),
    echa_lists          = paste(unique(na.omit(echa_lists)),          collapse = "|"),
    .groups = "drop"
  )

# Explode pipe-separated list memberships to long format for aggregation
chebi_lists_long <- chebi |>
  separate_rows(echa_lists, sep = "\\|") |>
  filter(echa_lists %in% valid_lists)

# Explode pipe-separated biological activity labels to long format
chebi_bio_long <- chebi |>
  separate_rows(bio_activity_labels, sep = "\\|") |>
  filter(!is.na(bio_activity_labels), bio_activity_labels != "")

# ------------------------------------------------------------------------------
# Priority scoring
# ------------------------------------------------------------------------------

# Regulatory list weights (see SCORING METHODOLOGY in the file header)
# Source for hierarchy: REACH Reg. (EC) No 1907/2006, Arts. 57-73;
#   CLP Reg. (EC) No 1272/2008; EU POPs Reg. (EU) 2019/1021;
#   Reg. (EC) No 1107/2009.
# Numerical scale (0-5): own methodological choice (see header).
list_weights <- c(
  # SVHC identification and Candidate List: REACH Art. 57-59
  candidate_list        = 5,  svhc_identification   = 5,
  # Restriction (Art. 67-73) and Authorisation (Art. 60-66): binding obligations
  restriction_list      = 4,  restriction_process   = 3,
  authorisation_list    = 4,  authorisation_process = 3,
  # POPs: elimination-level obligation; EU POPs Reg. (EU) 2019/1021
  pops_list             = 4,  pops_process          = 3,
  # PBT/vPvB assessment: REACH Art. 57(d)-(e), Annex XIII
  pbt_assessment        = 3,
  # ED assessment: REACH Art. 57(f); elevated by EU CSS COM(2020) 667
  ed_assessment         = 3,
  # Harmonised CLP classification: CLP Reg. (EC) No 1272/2008, Annex VI
  harmonised_list       = 2,  clh_process           = 2,
  # Pesticide approval: Reg. (EC) No 1107/2009
  pesticides            = 2,
  # Ongoing evaluation without final hazard determination
  substance_evaluation  = 1,  dossier_evaluation    = 1,
  # Positive list: approved use under active oversight, not a hazard signal
  eu_positive_list      = 1,
  # Baseline administrative requirements: no hazard determination
  reach_registrations   = 0,  sommatie_stoffen      = 0,
  anhydrous             = 0
)

# Bio-hazard weights, split by impact domain for use in Fig 14d.
# Tier-1 weights (3): aligned with SVHC criteria, REACH Art. 57(a)-(f).
# Tier-2 weights (2): own methodological addition (see header for justification).
# Tier-3 weights (1): own methodological addition (biological activity signal).
bio_hazard_health <- c(
  # CMR Cat 1A/1B: REACH Art. 57(a)-(c); CLP H350/H340/H360
  # All three CMR endpoints receive weight 3 — equal legal standing as SVHC criteria.
  # Note: ChEBI uses "teratogenic agent" (RO:0000087 role) rather than the CLP term
  # "reproductive toxicant"; teratogenicity is the ontologically proximate role for
  # reproductive/developmental toxicity in the ChEBI role hierarchy.
  "carcinogenic agent"  = 3,
  "genotoxin"           = 3,
  "mutagen"             = 3,
  "teratogenic agent"   = 3,   # REACH Art. 57(c) CMR endpoint; ChEBI label for reprotox
  # Own addition: neurotoxicity (weight 2) — EU CSS COM(2020) 667 priority hazard
  "neurotoxin"          = 2,
  # Own addition: allergen / drug allergen (weight 2) — CLP sensitisation Cat 1A
  "allergen"            = 2,
  "drug allergen"       = 2,
  # Own addition: sensitizer (weight 1) — CLP sensitisation Cat 1B, lower tier
  "sensitizer"          = 1,
  # Own addition: antimicrobial (weight 1) — biological activity signal
  "antimicrobial agent" = 1
)
bio_hazard_env <- c(
  # Persistent organic pollutant: proxy for PBT/vPvB, REACH Art. 57(d)-(e)
  "persistent organic pollutant" = 3,
  # Endocrine disruptor: REACH Art. 57(f); EU CSS COM(2020) 667
  "endocrine disruptor"          = 3,
  # Own addition: ecotoxin (weight 2) — PBT-adjacent, REACH Annex XIII
  "ecotoxin"   = 2,
  # Own addition: pesticidal activity (weight 1) — biological activity signal;
  # regulatory approval under Reg. (EC) No 1107/2009 already captured in list score
  "herbicide"  = 1, "insecticide" = 1, "fungicide" = 1,
  "acaricide"  = 1, "nematocide"  = 1
)
bio_weights <- c(bio_hazard_health, bio_hazard_env)

# Helper: identify the highest-consequence ECHA list category for a substance,
# used to colour bar charts and bubble charts.
top_list_category <- function(lists_str) {
  v <- str_split(lists_str, "\\|")[[1]]
  if (any(v %in% c("candidate_list", "svhc_identification")))      return("SVHC / Candidate")
  if (any(v %in% c("restriction_list", "restriction_process")))    return("Restriction")
  if (any(v %in% c("authorisation_list", "authorisation_process"))) return("Authorisation")
  if (any(v %in% c("pops_list", "pops_process")))                  return("POP")
  if (any(v %in% c("pbt_assessment")))                             return("PBT")
  if (any(v %in% c("ed_assessment")))                              return("ED")
  if (any(v %in% c("harmonised_list", "clh_process")))            return("Harmonised / CLP")
  if (any(v %in% c("pesticides")))                                 return("Pesticides")
  return("Other")
}

list_score_df <- chebi_lists_long |>
  mutate(weight = list_weights[echa_lists]) |>
  group_by(substance) |>
  summarise(
    list_score = sum(weight, na.rm = TRUE),
    n_lists    = n_distinct(echa_lists),
    .groups    = "drop"
  )

bio_score_df <- chebi_bio_long |>
  mutate(weight = bio_weights[bio_activity_labels]) |>
  group_by(substance) |>
  summarise(bio_score = sum(weight, na.rm = TRUE), .groups = "drop")

priority <- chebi |>
  left_join(list_score_df, by = "substance") |>
  left_join(bio_score_df,  by = "substance") |>
  mutate(
    list_score     = replace_na(list_score, 0),
    bio_score      = replace_na(bio_score, 0),
    n_lists        = replace_na(n_lists, 0L),
    priority_score = list_score + bio_score,
    top_list_cat   = map_chr(echa_lists, top_list_category)
  ) |>
  arrange(desc(priority_score))

