# ==============================================================================
# Fig 14g: Treemap - biological activity group vs. mean priority score
#
# INTENT
# ------
# Aggregates the priority score to the level of broad hazard categories
# (derived from ChEBI biological roles) and presents the result as a treemap.
# Rectangle area encodes the number of substances in each hazard group,
# giving a sense of how large each group is relative to the others.  Colour
# encodes the mean priority score within the group, indicating whether a
# hazard class is on average receiving appropriate regulatory attention.
# A large rectangle with a pale colour (many substances, low mean priority)
# may indicate a systematically under-regulated hazard class.  A small but
# dark rectangle (few substances, high mean priority) suggests that a niche
# but highly consequential group of substances is being addressed.
# ==============================================================================

