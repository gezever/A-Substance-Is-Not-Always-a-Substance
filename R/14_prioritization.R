# ==============================================================================
# 14_prioritization.R
# Analysis 14: Prioritisation of substances by regulatory weight and hazard profile
#
# PURPOSE
# -------
# Across European regulatory frameworks (REACH, CLP, Pesticides, OSPAR, etc.),
# thousands of chemical substances are listed, classified, and assessed.  Not
# all listings carry the same regulatory consequence: a substance that merely
# appears in the REACH registration database is treated very differently from
# one placed on the SVHC Candidate List or subject to an authorisation
# obligation.  Likewise, the biological role annotations derived from the ChEBI
# ontology (via `bash/chebi.sh`) provide an independent, semantically grounded
# signal of intrinsic hazard to human health and the environment.
#
# This script combines both dimensions into a composite priority score and
# produces a suite of visualisations that allow policymakers and data stewards
# to identify which substances deserve the most urgent attention, and to
# understand the structural relationships between hazard profiles and regulatory
# classification.
#
# DATA PROVENANCE
# ---------------
# `data/processed/chebi.csv` is generated by `bash/chebi.sh`:
#   1. The ChEBI OWL ontology is downloaded from EBI and converted to Turtle.
#   2. It is merged with the project's substance taxonomy
#      (`data/processed/rdf/substances_taxonomy.ttl`).
#   3. `exact_match_chebi.rq` links project substances to ChEBI entities via
#      InChIKey (skos:exactMatch).
#   4. `chebi_annotaties.rq` extracts biological roles (ChEBI property
#      RO:0000087 "has role") and stores them as OA Annotations.
#   5. `chebi_to_table.rq` exports a flat CSV via GROUP_CONCAT, yielding one
#      row per (substance, name) combination with pipe-separated lists of
#      biological activity labels and ECHA list memberships.
#
# Consequence: only substances with a known InChIKey receive biological
# activity labels (~21.5 % of rows).  Substances without a ChEBI match are
# scored on regulatory list membership alone, which is correct behaviour.
#
# METHODOLOGY
# -----------
# ## Established frameworks used as anchors
#
# | Framework | Role in methodology |
# |---|---|
# | **ToxPi** (Reif et al. 2010, *Environ Health Perspect* 118(12):1714–1720. DOI: 10.1289/ehp.1002180) | Conceptual basis for multi-domain weighted integration: heterogeneous evidence streams (regulatory listing + biological hazard) are combined into a single score through transparent, domain-specific weights. |
# | **REACH Regulation (EC) No 1907/2006, Arts. 57–73** | Legal hierarchy for list weights (0–5): SVHC identification (Art. 57–59) → authorisation/restriction (Art. 60–73).  The weight ranking is derived directly from the legal consequences each instrument triggers. |
# | **CLP Regulation (EC) No 1272/2008, Annex VI** | Basis for harmonised classification weights (weight 2) and CMR hazard weights (weight 3): carcinogenic, mutagenic, and reprotoxic properties are Tier-1 SVHC criteria under Art. 57(a)–(c). |
# | **EU POPs Regulation (EU) 2019/1021** | Basis for POPs list weight (4) and POP bio-hazard weight (3): elimination obligation for persistent organic pollutants is the strongest regulatory signal in the dataset. |
# | **EU Chemicals Strategy for Sustainability, COM(2020) 667 final** | Basis for including neurotoxicity and endocrine disruption as priority hazard classes (bio-hazard weight 2) even though they are not explicit SVHC criteria under Art. 57. |
# | **Regulation (EC) No 1107/2009** | Basis for pesticide approval list weight (2): positive safety determination required before approval. |
#
# ## Own methodological additions
#
# | Choice | Justification |
# |---|---|
# | Numerical scale 0–5 (lists) and 1–3 (bio-hazard) | Not prescribed by any framework; reflects the ordinal REACH hierarchy while remaining interpretable.  ToxPi uses normalised [0,1] domain scores; this project uses raw integers to preserve additivity without normalisation. |
# | Additive summation instead of ToxPi weighted average | The two domains are complementary and largely independent; additive summation avoids normalisation suppressing one domain when the other is zero.  The consequence is an unbounded score interpreted in relative, not absolute, terms. |
# | Neurotoxin and ecotoxin = weight 2 | EU CSS COM(2020) 667 identifies neurotoxicity as a priority hazard class; ecotoxicity is a PBT-adjacent concern (REACH Annex XIII).  Neither is an explicit Art. 57 criterion, so weight 2 (below CMR/POP weight 3) is justified. |
# | Allergen Cat 1A = weight 2; sensitiser Cat 1B = weight 1 | Harmonised CLH endpoint with serious health consequences; weight distinction between confirmed (1A) and probable (1B) sensitisation follows the CLP hazard category structure. |
# | `eu_positive_list` = weight 1 | Administrative oversight signal, not a hazard determination; weight 1 acknowledges regulatory scrutiny without implying concern. |
#
# ## Detailed scoring rationale
# (See the full narrative below for per-instrument legal citations.)
#
# The composite priority score combines two independent evidence domains:
# (1) regulatory list membership and (2) intrinsic biological hazard.
# The multi-domain structure is inspired by the ToxPi (Toxicological Priority
# Index) framework of Reif et al. (2010), which integrates heterogeneous
# evidence streams into a single prioritisation score through transparent,
# domain-specific weights:
#
#   Reif DM, Martin MT, Tan SW, Houck KA, Judson RS, Richard AM, Knudsen TB,
#   Dix DJ, Kavlock RJ. "Endocrine Profiling and Prioritization of
#   Environmental Chemicals Using ToxCast Data." Environ Health Perspect.
#   2010;118(12):1714-1720. DOI: 10.1289/ehp.1002180
#
# DOMAIN 1 — Regulatory list weights (0-5)
# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# The weight hierarchy is derived directly from the legal instruments
# established in REACH Regulation (EC) No 1907/2006 and related EU law.
# Each instrument represents a progressively stronger regulatory consequence:
#
#   5 — SVHC Candidate List / SVHC identification
#         Formal identification as a Substance of Very High Concern under
#         REACH Art. 57-59.  Triggers communication obligations (Art. 33)
#         and is a prerequisite for authorisation listing.
#         Source: REACH Reg. (EC) No 1907/2006, Art. 57-59.
#
#   4 — Restriction list / Authorisation list / POPs list
#         Restrictions (Art. 67-73) impose binding conditions or prohibitions.
#         Authorisation (Art. 60-66) requires prior approval for each use.
#         POPs listings impose the strictest environmental controls, including
#         elimination of production and use.
#         Sources: REACH Art. 60-73; EU POPs Regulation (EU) 2019/1021.
#
#   3 — Restriction process / Authorisation process / POPs process /
#       PBT assessment / ED assessment
#         Active regulatory scrutiny under an ongoing process; a final listing
#         determination has not yet been reached.  PBT and ED assessments
#         correspond directly to SVHC criteria under Art. 57(d)-(f).
#         Sources: REACH Art. 57(d)-(f), Annex XIII; REACH Art. 60-73.
#
#   2 — Harmonised CLP classification / CLH process / Pesticide approval
#         Harmonised classifications (CLP Reg. (EC) No 1272/2008, Annex VI)
#         establish legally binding hazard categories, particularly CMR.
#         Pesticide approvals under Reg. (EC) No 1107/2009 confirm biological
#         activity and require a positive safety determination.
#         Sources: CLP Reg. (EC) No 1272/2008, Annex VI;
#                  Reg. (EC) No 1107/2009.
#
#   1 — Dossier evaluation / Substance evaluation / EU positive list
#         Regulatory scrutiny without a final hazard determination.  Positive
#         lists indicate an approved use under active oversight rather than a
#         hazard signal per se.
#
#   0 — REACH registration / sommatie_stoffen / anhydrous
#         Baseline administrative requirements with no inherent hazard
#         determination.  Included to ensure completeness of list membership.
#         Source: REACH Reg. (EC) No 1907/2006, Title II.
#
# OWN METHODOLOGICAL ADDITION — numerical scale:
#   The specific integers (0-5) are not prescribed by any regulatory text or
#   by ToxPi.  They were chosen to reflect the ordinal regulatory hierarchy
#   while keeping the scale interpretable.  The ratio 5:0 (SVHC : registration)
#   mirrors the qualitative difference in legal consequence.
#
# DOMAIN 2 — Biological hazard score (1-3)
# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# Bio-activity labels are derived from ChEBI biological roles via the
# ontology pipeline (bash/chebi.sh) and reflect intrinsic hazard properties
# independent of any regulatory determination.  The weight tier is aligned
# with the SVHC hazard criteria of REACH Art. 57:
#
#   3 — CMR Cat 1A/1B: carcinogenic, mutagenic, reproductively toxic
#         These are Tier-1 SVHC properties under REACH Art. 57(a)-(c) and
#         correspond to CLP hazard categories H350, H340, H360.
#         All three CMR endpoints receive equal weight (3) consistent with
#         their equal legal standing as SVHC criteria.
#         Source: REACH Art. 57(a)-(c); CLP Reg. (EC) No 1272/2008.
#
#   3 — Persistent organic pollutant / Endocrine disruptor
#         POP is a proxy for PBT/vPvB properties (REACH Art. 57(d)-(e)).
#         Endocrine disruption corresponds to REACH Art. 57(f) and was
#         elevated to equivalent concern in the EU Chemicals Strategy for
#         Sustainability (European Commission, 2020).
#         Source: REACH Art. 57(d)-(f); EU CSS COM(2020) 667 final.
#
#   2 — Neurotoxin / Ecotoxin / Allergen (sensitisation Cat 1A)
#         OWN METHODOLOGICAL ADDITION: These properties are not among the
#         explicit SVHC criteria of Art. 57.  They are included because:
#         - Neurotoxicity is identified as a priority hazard class in the
#           EU CSS (European Commission, 2020, p. 9).
#         - Ecotoxicity is a recognised PBT-adjacent concern under REACH
#           Annex XIII and is central to environmental prioritisation.
#         - Sensitisation (allergen, CLP Cat 1A) is a harmonised CLH endpoint
#           with serious health consequences, treated here as intermediate.
#         Weight 2 was chosen to distinguish these from Tier-1 SVHC properties
#         while acknowledging their regulatory relevance.
#
#   1 — Sensitizer (Cat 1B) / Antimicrobial / Pesticidal activity
#         OWN METHODOLOGICAL ADDITION: Biological activity indicating
#         interaction with living systems (environmental or pharmacological)
#         but with lower systemic concern for humans than CMR/neurotoxic
#         properties.  Weight 1 signals biological relevance without implying
#         SVHC-level concern.
#
# AGGREGATION — additive summation
# ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# OWN METHODOLOGICAL ADDITION: ToxPi uses a weighted average of normalised
# domain scores (each domain rescaled to [0, 1]).  Here, the two domains are
# conceptually complementary rather than redundant: regulatory listing and
# intrinsic hazard measure different things and are largely independent
# (a substance may have many list memberships but lack ChEBI bio-activity data,
# or vice versa).  Additive summation preserves this independence and prevents
# normalisation from suppressing one domain when the other is zero.  The
# consequence is that the score is not bounded and must be interpreted in
# relative rather than absolute terms.
#
# Priority score = sum(list weights) + sum(bio-hazard weights)
#
# INTERPRETATION
# --------------
# **Fig 14a — Top-50 priority bar chart**
# The ranked bar chart answers: "which substances are most urgent?"  A high
# score reflects broad multi-list membership AND/OR strong hazard evidence.
# Substances near the top that are well-known SVHCs (BPA, acrylamide, phthalates)
# validate the scoring model; unexpected entries near the top warrant manual
# review to check whether the score is driven by data artefacts (e.g., a CAS
# number mapped to many InChIKeys).
#
# **Fig 14b — ECHA list presence heatmap (top 50)**
# Reveals which instruments drive each substance's priority score.  A row with
# many red cells = broad multi-list substance.  A row with one red cell = score
# driven by a single high-weight instrument; if that instrument is authorisation
# or restriction, the substance is a genuine concern; if it is registration,
# the weight contribution is minimal and the high rank is likely driven by
# bio-hazard evidence.
#
# **Fig 14c — UpSet plot (concern lists)**
# Shows how substances distribute across the concern-level lists only.
# Tall multi-list intersection bars = a chemical core that multiple binding
# instruments converge on — these are the highest regulatory priority.
# Source-specific bars identify substances that have fallen through gaps in
# cross-list harmonisation.
#
# **Fig 14d — Hazard profile bar chart**
# Answers: "what hazard types are most prevalent?"  A dominant CMR bar means
# that carcinogenic/mutagenic/reprotoxic properties drive most bio-hazard
# scores.  A large "no bio evidence" share confirms the ~79 % non-InChIKey
# problem: most substances cannot receive a bio-hazard score at all.
#
# **Fig 14e — Bubble chart (list score × bio score)**
# The four quadrants (SKILL.md §10) identify: Q1 = urgent (both dimensions
# high); Q2 = regulatory gap (high bio, low lists); Q3 = administrative breadth
# (high lists, no bio evidence); Q4 = low concern.  Q2 substances are the most
# actionable finding: hazard is confirmed but regulatory coverage is weak.
#
# **Fig 14f — Alluvial / Sankey diagram**
# Traces which hazard types flow to which regulatory instruments.  A wide band
# from "carcinogenic agent" to "candidate_list" confirms the CMR → SVHC
# pathway.  Hazard types without a wide band to any high-weight instrument
# indicate that the regulatory system has not fully captured that hazard class.
#
# **Fig 14g — Treemap (hazard classes)**
# Tile area = number of substances in the class; colour = mean priority score.
# Large tiles with low colour intensity = common but under-prioritised classes
# (regulatory gap candidates).  Small tiles with high colour intensity = rare
# but highly regulated classes.
#
